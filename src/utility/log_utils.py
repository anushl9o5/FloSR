def fig2img(fig_buffer):
    buf = io.BytesIO()
    plt.axis('off')
    plt.savefig(buf, format="png",transparent = True, bbox_inches = 'tight', pad_inches = 0,dpi=100, facecolor=(0, 0, 0))
    buf.seek(0)
    img_arr = np.frombuffer(buf.getvalue(), dtype=np.uint8)
    buf.close()
    img = cv2.imdecode(img_arr, 1)
    return img

